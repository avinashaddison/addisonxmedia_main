import { execSync } from 'child_process';
import { writeFileSync, readFileSync, rmSync } from 'fs';
import { resolve } from 'path';

const srcDir = resolve('server/routes');
const outputTs = resolve(srcDir, 'site-static-css.ts');
const tempCss = resolve(srcDir, 'temp-compiled.css');

console.log('Compiling public site CSS with Tailwind...');
try {
  execSync(
    `npx tailwindcss -i "${resolve(srcDir, 'site-public.css')}" -o "${tempCss}" --content "${resolve(srcDir, 'site-public.ts')}" --minify`,
    { stdio: 'inherit' }
  );

  const cssContent = readFileSync(tempCss, 'utf-8').trim();
  const tsContent = `// Automatically generated by scripts/compile-site-css.mjs. Do not edit manually.\nexport const siteStaticCss = ${JSON.stringify(cssContent)};\n`;

  writeFileSync(outputTs, tsContent, 'utf-8');
  console.log('Successfully compiled CSS to server/routes/site-static-css.ts');
} catch (error) {
  console.error('Error compiling public site CSS:', error);
  process.exit(1);
} finally {
  try {
    rmSync(tempCss, { force: true });
  } catch {}
}
